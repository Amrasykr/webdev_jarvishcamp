<?php

// array function

$names = ['Ammar', 'Asysyakur', 'Abdul', 'Latif'];


// fungsi untuk menambahkan data ke dalam array
array_push($names, 'Ammar');

// fungsi untuk menghapus data terakhir dari array
array_pop($names);

// fungsi untuk menghapus data pertama dari array
array_shift($names);

// fungsi untuk menambahkan data ke dalam array
array_unshift($names, 'Ammar');


print_r($names);
