<?php

// operatior kondisional

$firstNumber = 10;
$secondNumber = 5;
$thirdNumber = 7;
$fourthNumber = 7;


// operator and
if ($firstNumber == $secondNumber && $thirdNumber == $fourthNumber) {
    echo "True <br>";
} else {
    echo "False <br>";
}


// operator or
if ($firstNumber == $secondNumber || $thirdNumber == $fourthNumber) {
    echo "True <br>";
} else {
    echo "False <br>";
}
